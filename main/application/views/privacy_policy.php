5- How to Access, Update and Manage Your Information
Information We Receive From a Social Network Where You Participate in our competition or place a Vote From Your Mobile Device
To manage the information CAAN receives about you from a social network, like Facebook, where you share our content or from where you upload content, 
you will need to follow the instructions at that site for updating your information and changing your privacy settings. The privacy management tools 
for applications on Facebook can be found here. To review and update information associated with your CAAN game profile in certain games, visit the 
“settings” page in that game. You also can manage some aspects of information collection and use by visiting the “settings” page of your mobile 
device and reviewing the permissions of each application or “app.”

Once CAAN receives your information from a social network or your mobile device, that information is stored and used by CAAN in accordance with this 
Privacy Policy. You may access and update that information as described below.

Accessing and Updating Your Information Held by CAAN
If you want to review, delete, or change the information CAAN has about you or have additional questions, e-mail us. We will respond to your request 
within thirty days.

Stopping Use of Your Information
If you no longer want CAAN to make active use of your information, you may send an e-mail through the “contact us” portion of our website. Place 
"Delete My Account" in the subject line and include your first name, last name, e-mail address and your social network ID for the social network from 
which you access our Services (if applicable) in the body of the e-mail (for example, your Facebook user ID). We will respond to your request within 
thirty days. Please note that certain records, for example those relating to payments or customer service matters, will be held for legal and 
accounting purposes. If you have sent content through or posted content on the Service, we may not be able to delete it.

Opting Out of Geolocation
If you have previously allowed us to access your geolocation data, you can stop making geolocation available to us by visiting your mobile device's 
settings for the relevant application or the “settings” page for the relevant game.

Opting Out of Promotional Emails from CAAN
If you want to stop receiving promotional e-mails from CAAN, click on the "unsubscribe" link in any promotional email from CAAN or visit the e-mail 
preference page. Please note that once we receive your request, it may take an additional period of time for your opt-out to become effective. Your 
unsubscribe or e-mail preference change will be processed promptly, and in no event longer than ten business days. Please note that by accepting 
these terms of service, it is understood and agreed that you consent to receiving emails and communications from CAAN with regard to Athlete 
Nominations, in accordance with CASL legislation. You can opt out of receiving such communication at any time.

Opting Out of Other Communications
You can opt out of receiving SMS messages (for example, text messages) by texting back STOP to the number we send the text from for that particular 
SMS message program. In some cases a preferences management page will also be available. When we receive an opt-out message from you for SMS 
messages, we may send a message confirming our receipt of your opt-out.

When you install our apps on your mobile device you can choose to receive push notifications, which are messages an app sends you on your mobile 
device even when the app is not on. You can turn off notifications by visiting your mobile device’s “settings” page.

Note that if you opt-out of our Promotional Communications or other forms of communication, we may still e-mail or communicate with you from time to 
time if we need to provide you with information about the Service or if we need to request information from you with respect to a transaction 
initiated by you or for other legitimate non-marketing reasons, for example, if we update this Privacy Policy or our Terms of Service.

Opting Out of Third Party Tailored Advertising 
If you are interested in more information about tailored advertising and your choices to prevent third parties from delivering tailored web and 
mobile web advertising you may visit the following websites:

Network Advertising Initiative Consumer Opt-Out Page or the Digital Advertising Alliance Opt-Out Page for U.S.-based advertising; and
Your Online Choices UK website for EU-based advertising.
These opt-out tools are provided by third parties, not CAAN. CAAN does not control or operate these tools or the choices that advertisers and others 
provide through these tools.

In addition, if you do not want to receive tailored in-application advertisements from third parties that relate to your interests in apps on your 
mobile device, you may opt-out by adjusting the ad tracking settings on your device. In addition, you can reset the “Advertising Identifier” (like an 
IDFA) from your mobile device’s settings page, which will prevent continued use of existing behavioral data tied to the previous “Advertising 
Identifier.”

Please note that you may still receive in-app advertisements even if you opt-out of tailored advertising but they will not be based on your activity 
across unrelated web sites or apps.

Opting Out of Cookie Tracking

You can set your web browser to warn you about attempts to place cookies on your computer or limit the type of cookies you allow. Flash cookies 
operate differently than browser cookies and cookie management tools available in a web browser may not remove flash cookies. To learn more about and 
manage flash cookies you can visit the Adobe website and make changes at the Global Privacy Settings Panel.

If you disable cookies, you may lose some of the features and functionality of our Services because CAAN cookies are necessary to track and enhance 
your game activities.
