2 - Information We Collect and How We Collect It

Information about you that we get from social networks (like Facebook):

If you participate in any competition arranged by CAAN, or upload any video, or place a comment on any video, or otherwise access any of our other 
Services on a social network like Facebook, CAAN receives certain information about you from the social network automatically. The information we 
receive depends on the CAAN content you’re accessing, the information you input, the social network, and your privacy settings and your friends’ 
privacy settings on that social network.

For example, CAAN may collect and store some or all of the following information provided by the social network:

Your first and last name
Your profile picture or its URL
Your social network ID number (like your Facebook ID number), which is linked to publicly-available information like your name and profile photo
The social network ID numbers and other public data for your friends
The login e-mail you provided to that social network when you registered with it
Your physical location and that of the devices you use to access our Services
Your gender
Your birthday and/or age range
Other publicly-available information on the social network; and/or
any other information that you or the social networks share with CAAN.

If you access our Services from a social network, you should also read that social network’s Terms of Service and Privacy Policy.

If you are unclear about what information a social network is sharing with us, please go to the social networks where you play access our content to 
find out more about their privacy settings.

Cookies and Automated Information Collection
We and service providers acting on our behalf, like Google Analytics, store log files and use tracking technologies such as:
Cookies, which are small pieces of data transferred to your mobile device or computer for record-keeping purposes
Web beacons, which let us know if a certain page was visited or whether an e-mail was opened
Tracking pixels, which allow us or our advertising partners to advertise more efficiently and effectively
Local shared objects, also known as flash cookies, which help us to reduce fraud, remember your in-app preferences and speed up load times.

We and our service providers use these log files, tags, and tracking technologies to collect and analyze certain kinds of technical information, 
including:
IP addresses
The type of computer or mobile device you are using
Your operating system version
Your mobile device’s identifiers, like your MAC Address, Identifier For Advertising (IDFA), and/or International Mobile Equipment Identity (IMEI)
Your browser types
Your browser language
Referring and exit pages, and URLs
Platform type
The number of clicks on a page or feature
Domain names
Landing pages
Pages viewed and the order of those pages
The amount of time spent on particular pages
Province and the date and time of activity on our websites or apps.

In some cases, we will connect this information with your social network ID or CAAN user ID.

Please note that companies delivering advertisements on our Services may also use cookies or other technologies as described below in Third Party 
Advertising Including Tailored Advertising and Analytics, and those practices are subject to those companies’ own policies.

For more information on how to limit the types of cookies you allow, please see Opting out of Cookie Tracking.

Other Information from Your Mobile Device:
If you upload videos to the CAAN app or participate in discussions on your mobile device, in addition to your device identifiers, we may also collect:
The name you have associated with your device
Your telephone number
Your country
With your permission, your specific geolocation and your mobile contacts (as further described below in “Information About Your Contacts”).

If you want to adjust your privacy preferences on your mobile device, please see How to Access, Update and Manage Your Information.

Information About You That You Share With Us Directly:
When you use our Services (whether through a social network or through CAAN directly), you may give us information directly (like when you’re setting 
up your account) and we will store that on our systems and use it for the purposes described in this Privacy Policy.

Some areas of our app or parts of our Services may use a more traditional registration or account set-up process where you may be asked to give us 
some or all of the following information:
Your age or birthday
Your first and last names
Your e-mail address
A password
Other information that helps us make sure it’s you accessing your account or helps us improve our services.
We may also let you create a participant profile, separate from your social networking site profile (for example, your Facebook profile), that other 
CAAN app members can see. Your participant profile may include information like:
A profile photo
Username(s)
Your gender
Biographic details (like your age or age range)
Approximate location information that you provide
Links to your profiles on various social network
Details about the content you have uploaded
A CAAN ID number that is created by CAAN and used to identify your profile.

Certain information in your CAAN profile will be publicly accessible on CAAN’s websites that offer direct access to CAAN competition and content, 
including:
The CAAN competition participant and commentor ID
Your first and last names
Your profile picture.

Your CAAN app ID number used to identify your account and profile will appear in the URL of your profile page. It will only allow access to 
information you make public in your settings.

Information About Your Contacts:
We may give you the ability to import your address book contacts or manually enter them so that you can locate your contacts on CAAN and invite them 
to join you in commenting and voting for your content or other aspects of our Services. You are responsible for getting your contacts' permission 
when you opt to give their contact details to us. We may also get information about you from other CAAN users importing or entering their contacts. 
We use and may store this contact information to help you and your contacts connect through our Services. If you want to remove your contacts stored 
by us, follow the instructions provided in the app and website to request that we remove them from our systems. If you need to enter a password to 
allow us to access your address book, we will not store that password.

Information You Generate Using Our Communications Features:
You may be able to take part in certain activities on our Service that let you communicate or share information not just with CAAN, but also with 
other CAAN competition participants. These include:
Participating in forums and message boards
Posting public comments to other peoples’ profiles
Sending private messages or invitations to other participants and members, either directly on our websites or to their e-mail accounts
Chatting with other participants
Posting photos or drawings and videos.

We record and store archives of these communications, comments, photos, videos and drawings on CAAN's servers to protect the safety and well-being of 
our participants and CAAN's rights and property in connection with our Services and you acknowledge and agree to that.


Customer Support Correspondence:
When you ask for help from our Customer Support team, we will collect and store the contact information you give them (generally, your name and 
e-mail address), information about your activity on our Services, and your CAAN participant and/or social network ID number. We will also store the 
communications you have with the Customer Service team and any information in those communications in order to provide support.

Information You Give Us For Text Messaging Services
We receive and store the information you provide, including your mobile telephone number, when you choose to sign up to receive SMS messages.


Other Sources
We may collect or receive information about you from other sources like third party information providers. We use this information along with 
information you provide us directly, for example, to help you and your friends connect or to serve you advertising more tailored to your interests.

If you would like to learn more about how we keep your information secure, please see Security of Your Information.
