used to access our Services after you use it.

While we take reasonable precautions against possible security breaches of our Services and our customer databases and records, no website or 
Internet transmission is completely secure. Although we strive to protect your personal information, we cannot guarantee that unauthorized access, 
hacking, data loss, or other breaches will never occur, we cannot guarantee the security of your information while it is being transmitted to our 
Service. Any transmission is at your own risk. 


