profile photo. We may also send promotional e-mail messages and promotional SMS messages (e.g. text messages) ("Promotional Communications") directly 
or in partnership with other parties, in accordance with your marketing preferences. Each Promotional Communication will offer you choices about 
receiving additional messages.

